function srtbut() {
    let srt = document.getElementById("srt")
    if (srt.style.backgroundColor == "rgb(50, 74, 179)") {
        srt.style.backgroundColor = "#30217E";
    } else {
        srt.style.backgroundColor = "#324ab3"
    }
}

